/// Returns up to 20 questions for an assessment.
///
/// If [weekSkillTags] is provided (weekly assessment), questions are
/// weighted heavily toward those tags (70%) with the remainder from the
/// broader level pool for variety (30%).
///
/// If [weekSkillTags] is null or empty (free assessment), questions are
/// spread evenly across all tags at that level.
List<Question> getQuestionsByLevel(String level,
    {List<String>? weekSkillTags}) {
  const levelAlias = {
    'beginner':     'beginner',
    'elementary':   'elementary',
    'intermediate': 'intermediate',
    'upper':        'upper',
    'secondary':    'secondary',
    'senior':       'senior',
    // legacy fallback
    'advanced':     'secondary',
  };
  final resolvedLevel = levelAlias[level.toLowerCase()] ?? level;
  final fullPool =
  phonicsQuestions.where((q) => q.level == resolvedLevel).toList();

  const int target = 20;

  // ── Weekly assessment: align to week's skill tags ──────────────────────
  if (weekSkillTags != null && weekSkillTags.isNotEmpty) {
    final focal = fullPool
        .where((q) => weekSkillTags.contains(q.skillTag))
        .toList()
      ..shuffle();
    final other = fullPool
        .where((q) => !weekSkillTags.contains(q.skillTag))
        .toList()
      ..shuffle();

    final focalCount = (target * 0.7).round();
    final otherCount = target - focalCount;

    final selected = <Question>[
      ...focal.take(focalCount),
      ...other.take(otherCount),
    ]..shuffle();

    // If focal pool is small, pad with extra from other
    if (selected.length < target) {
      final remaining =
      fullPool.where((q) => !selected.contains(q)).toList()..shuffle();
      selected.addAll(remaining.take(target - selected.length));
    }

    return selected.take(target).toList();
  }

  // ── Free assessment: spread evenly across all tags ─────────────────────
  final Map<String, List<Question>> byTag = {};
  for (final q in fullPool) {
    byTag.putIfAbsent(q.skillTag, () => []).add(q);
  }
  for (final list in byTag.values) {
    list.shuffle();
  }

  final tags = byTag.keys.toList()..shuffle();
  final int perTag = (target / tags.length).ceil();

  final List<Question> selected = [];
  for (final tag in tags) {
    selected.addAll(byTag[tag]!.take(perTag));
  }

  selected.shuffle();
  return selected.take(target).toList();
}

/// Alias used by QuestionService to avoid name collision with the
/// class method of the same name.
List<Question> getQuestionsForLevelFromPool(String level,
    {List<String>? weekSkillTags}) =>
    getQuestionsByLevel(level, weekSkillTags: weekSkillTags);

